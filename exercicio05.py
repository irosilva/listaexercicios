 # import math
print ('*'*50)
numero1 = float(input('digite um numero : '))
numero2 = float(input('digite outro numero : '))
print ('*'*50)
 
# processamento

adicao= numero1+numero2
subtracao= numero1-numero2
multiplicacao= numero1*numero2
divisao= numero1/numero2

print (f'a soma dos dois numeros é :{adicao:.2f}')
print (f'a diferença dos dois numeros é :{subtracao:.2f}')
print (f'a multiplicaçao dos dois numeros é :{multiplicacao:.2f}')
print (f'a divisão dos dois numeros é :{divisao:.2f}')